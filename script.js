copyButtons.forEach((button) => {
  button.addEventListener("click", async () => {
    const email = button.getAttribute("data-copy");
    const dictionary = translations[currentLanguage];

    if (!email) {
      return;
    }

    try {
      await navigator.clipboard.writeText(email);
      button.textContent = dictionary.copiedEmail;
      button.classList.add("copied");

      window.setTimeout(() => {
        button.textContent = dictionary.copyEmail;
        button.classList.remove("copied");
      }, 1600);
    } catch {
      window.location.href = `mailto:${email}`;
    }
  });
});

function setText(selector, text) {
  const element = document.querySelector(selector);

  if (element && text) {
    element.textContent = text;
  }
}

function renderSummary(selector, summary) {
  const element = document.querySelector(selector);

  if (!element) {
    return;
  }

  const paragraphs = Array.isArray(summary) ? summary : [summary];
  element.replaceChildren();

  paragraphs.filter(Boolean).forEach((paragraph) => {
    const p = document.createElement("p");
    p.textContent = paragraph;
    element.append(p);
  });
}

