function renderProjectVisual(container, visual) {
  container.replaceChildren();

  if (!visual || visual.type === "eco") {
    container.append(createEcoVisual());
    return;
  }

  if (visual.type === "booking") {
    container.append(createBookingVisual());
    return;
  }

  if (visual.type === "image") {
    const figure = document.createElement("figure");
    const image = document.createElement("img");
    figure.className = "project-shot case-shot";
    image.src = visual.src;
    image.alt = visual.alt || "";
    image.loading = "lazy";
    image.width = 1440;
    image.height = 900;
    figure.append(image);
    container.append(figure);
  }
}

function renderMetaList(container, items) {
  container.replaceChildren();

  items.forEach(([term, description]) => {
    const wrapper = document.createElement("div");
    const termElement = document.createElement("dt");
    const descriptionElement = document.createElement("dd");
    termElement.textContent = term;
    descriptionElement.textContent = description;
    wrapper.append(termElement, descriptionElement);
    container.append(wrapper);
  });
}

function renderCardList(container, items) {
  container.replaceChildren();

  items.forEach((item) => {
    const article = document.createElement("article");
    const title = document.createElement("h3");
    const body = document.createElement("p");
    title.textContent = item.title;
    body.textContent = item.body;
    article.append(title, body);
    container.append(article);
  });
}

function renderLinks(container, links) {
  container.replaceChildren();

  if (!links.length) {
    container.hidden = true;
    return;
  }

  container.hidden = false;

  links.forEach((linkItem) => {
    const link = document.createElement("a");
    link.className = linkItem.primary ? "project-action primary" : "project-action";
    link.href = linkItem.href;
    link.textContent = linkItem.label;

    if (/^https?:\/\//i.test(linkItem.href)) {
      link.target = "_blank";
      link.rel = "noreferrer";
    }

    container.append(link);
  });
}

function renderGallery(section, container, screenshots = []) {
  if (!section || !container) {
    return;
  }

  container.replaceChildren();

  if (!screenshots.length) {
    section.hidden = true;
    return;
  }

  section.hidden = false;

  screenshots.forEach((screenshot) => {
    const figure = document.createElement("figure");
    const image = document.createElement("img");
    const caption = document.createElement("figcaption");

    image.src = screenshot.src;
    image.alt = screenshot.alt || "";
    image.loading = "lazy";
    image.width = 1440;
    image.height = 900;
    caption.textContent = screenshot.caption || "";

    figure.append(image, caption);
    container.append(figure);
  });
}

function getProjectContent(language) {
  const catalog = projectCaseStudies[language] || projectCaseStudies.en;
  const params = new URLSearchParams(window.location.search);
  const rawProjectId = params.get("project") || "ecowing";
  const normalizedProjectId = projectAliases[rawProjectId] || rawProjectId;
  const project = catalog.projects[normalizedProjectId] || catalog.projects.ecowing;

  return { catalog, project };
}

