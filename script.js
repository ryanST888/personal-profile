const reducedMotionQuery = window.matchMedia("(prefers-reduced-motion: reduce)");

function initWorkbenchTilt() {
  const visual = document.querySelector(".portrait-visual");
  const shell = document.querySelector("[data-tilt]");

  if (!visual || !shell || reducedMotionQuery.matches) {
    return;
  }

  visual.addEventListener("pointermove", (event) => {
    const rect = visual.getBoundingClientRect();
    const x = (event.clientX - rect.left) / rect.width - 0.5;
    const y = (event.clientY - rect.top) / rect.height - 0.5;

    shell.style.setProperty("--tilt-y", `${x * 8}deg`);
    shell.style.setProperty("--tilt-x", `${y * -7}deg`);
  });

  visual.addEventListener("pointerleave", () => {
    shell.style.setProperty("--tilt-y", "0deg");
    shell.style.setProperty("--tilt-x", "0deg");
  });
}

function initHeroCanvas() {
  const canvas = document.querySelector("#hero-canvas");

  if (!(canvas instanceof HTMLCanvasElement)) {
    return;
  }

  const context = canvas.getContext("2d");

  if (!context) {
    return;
  }

  const pointer = { active: false, x: 0, y: 0 };
  let particles = [];
  let animationFrame = 0;

  function createParticles(width, height) {
    const particleCount = Math.min(58, Math.max(28, Math.floor((width * height) / 9200)));

    particles = Array.from({ length: particleCount }, () => ({
      x: Math.random() * width,
      y: Math.random() * height,
      radius: Math.random() * 1.8 + 1.2,
      vx: (Math.random() - 0.5) * 0.34,
      vy: (Math.random() - 0.5) * 0.34,
    }));
  }

  function resize() {
    const rect = canvas.getBoundingClientRect();
    const ratio = Math.min(window.devicePixelRatio || 1, 2);
    const width = Math.max(1, rect.width);
    const height = Math.max(1, rect.height);

    canvas.width = Math.floor(width * ratio);
    canvas.height = Math.floor(height * ratio);
    context.setTransform(ratio, 0, 0, ratio, 0, 0);
    createParticles(width, height);
  }

  function drawConnections(width, height) {
    context.clearRect(0, 0, width, height);

    particles.forEach((particle, index) => {
      if (!reducedMotionQuery.matches) {
        particle.x += particle.vx;
        particle.y += particle.vy;
      }

      if (particle.x < 0 || particle.x > width) {
        particle.vx *= -1;
      }

      if (particle.y < 0 || particle.y > height) {
        particle.vy *= -1;
      }

      for (let nextIndex = index + 1; nextIndex < particles.length; nextIndex += 1) {
        const nextParticle = particles[nextIndex];
        const distance = Math.hypot(particle.x - nextParticle.x, particle.y - nextParticle.y);

        if (distance < 88) {
          context.strokeStyle = `rgba(31, 107, 91, ${0.22 * (1 - distance / 88)})`;
          context.lineWidth = 1;
          context.beginPath();
          context.moveTo(particle.x, particle.y);
          context.lineTo(nextParticle.x, nextParticle.y);
          context.stroke();
        }
      }

      if (pointer.active) {
        const pointerDistance = Math.hypot(particle.x - pointer.x, particle.y - pointer.y);

        if (pointerDistance < 120) {
          context.strokeStyle = `rgba(201, 111, 58, ${0.26 * (1 - pointerDistance / 120)})`;
          context.beginPath();
          context.moveTo(particle.x, particle.y);
          context.lineTo(pointer.x, pointer.y);
          context.stroke();
        }
      }

      context.fillStyle = "rgba(31, 107, 91, 0.52)";
      context.beginPath();
      context.arc(particle.x, particle.y, particle.radius, 0, Math.PI * 2);
      context.fill();
    });
  }

  function tick() {
    drawConnections(canvas.clientWidth, canvas.clientHeight);
    animationFrame = window.requestAnimationFrame(tick);
  }

  canvas.addEventListener("pointermove", (event) => {
    const rect = canvas.getBoundingClientRect();
    pointer.active = true;
    pointer.x = event.clientX - rect.left;
    pointer.y = event.clientY - rect.top;
  });

  canvas.addEventListener("pointerleave", () => {
    pointer.active = false;
  });

  window.addEventListener("resize", resize);
  resize();

  if (reducedMotionQuery.matches) {
    drawConnections(canvas.clientWidth, canvas.clientHeight);
    return;
  }

  animationFrame = window.requestAnimationFrame(tick);

  window.addEventListener("beforeunload", () => {
    window.cancelAnimationFrame(animationFrame);
  });
}

function initGsapAnimations() {
  if (reducedMotionQuery.matches || !window.gsap) {
    return;
  }

  const { gsap } = window;

  if (window.ScrollTrigger) {
    gsap.registerPlugin(window.ScrollTrigger);
  }

  if (document.querySelector(".hero")) {
    gsap
      .timeline({ defaults: { duration: 0.72, ease: "power3.out" } })
      .from(".hero .eyebrow", { opacity: 0, y: 18 })
      .from("h1", { opacity: 0, y: 26 }, "-=0.54")
      .from(".hero-statement", { opacity: 0, y: 24 }, "-=0.5")
      .from(".hero-lede", { opacity: 0, y: 20 }, "-=0.48")
      .from(".hero-actions .button", { opacity: 0, y: 18, stagger: 0.08 }, "-=0.42")
      .from(".hero-contact-card", { opacity: 0, y: 18 }, "-=0.38")
      .from(".hero-stack span", { opacity: 0, y: 14, stagger: 0.08 }, "-=0.34")
      .from(".portrait-card", { opacity: 0, rotateX: 8, y: 34 }, "-=0.64")
      .from(".floating-chip", { opacity: 0, scale: 0.82, stagger: 0.1 }, "-=0.48");

    gsap.to(".floating-chip", {
      duration: 2.6,
      ease: "sine.inOut",
      repeat: -1,
      stagger: 0.22,
      y: -12,
      yoyo: true,
    });
  }

  if (!window.ScrollTrigger) {
    return;
  }

  gsap.utils
    .toArray(
      ".section-heading, .education-panel, .timeline-item, .project-card, .skills-layout > div, .activity-card, .case-hero, .case-proof-grid > article, .case-gallery > figure, .case-story > article, .case-next",
    )
    .forEach((element, index) => {
      gsap.from(element, {
        duration: 0.66,
        ease: "power3.out",
        opacity: 0,
        scrollTrigger: {
          start: "top 86%",
          trigger: element,
        },
        y: 28,
        delay: Math.min(index * 0.025, 0.12),
      });
    });
}

